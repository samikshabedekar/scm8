.MODEL SMALL
.STACK
.DATA
M1 DB 10,13,"Enter String 1: $"
M2 DB 10,13,"Enter String 2: $"
M3 DB 10,13,"String 1: $"
M4 DB 10,13,"Length of String 1 : $"
M5 DB 10,13,"String 2: $"
M6 DB 10,13,"Length of String 2 : $"
M7 DB 10,13,"Concatenated String: $"
M8 DB 10,13,"Length after Concatenation: $"
M9 DB 10,13,"Comparison: $"
M10 DB 10,13,"String 1 and String 2 are Equal. $"
M11 DB 10,13,"String 1 and String 2 are not Equal. $"
N1 DB ?
N2 DB ?
N3 DB ?
S1 DB 50,?,50 DUP(0)
S2 DB 50,?,50 DUP(0)
S3 DB 100,?,100 DUP(0)

.CODE

DISP_ASCII MACRO XX
MOV AH,02
MOV DL,XX           
INT 21H
ENDM

DISP_WORD MACRO XX
MOV AH,09
LEA DX,XX
INT 21H
ENDM

GET_STRING MACRO XX
MOV AH,0AH
LEA DX,XX
INT 21H
ENDM



DISPLAY_STRING MACRO XX
LOCAL DISP_LOOP
LEA SI,XX + 1
MOV CL,[SI]
INC SI
DISP_LOOP:
MOV BL,[SI]
DISP_ASCII BL
INC SI
DEC CL
JNZ DISP_LOOP
ENDM

.STARTUP


CALL displayStrings
CALL concatenateStrings
CALL checkEqual
JMP LAST


displayStrings PROC
DISP_WORD M1
GET_STRING S1
LEA SI,S1+1
MOV CL,[SI]
MOV N1,CL
DISP_WORD M2
GET_STRING S2
LEA DI,S2+1
MOV CH,[DI]
MOV N2,CH

DISP_WORD M3
DISPLAY_STRING S1
MOV BL,N1
ADD BL,30H
DISP_WORD M4
DISP_ASCII BL


DISP_WORD M5
DISPLAY_STRING S2
MOV BH,N2
ADD BH,30H
DISP_WORD M6
DISP_ASCII BH 

RET
ENDP

concatenateStrings PROC
LEA DI,S3+1
MOV DL,N1
MOV DH,N2
MOV [DI],DL
ADD [DI],DH

INC DI
LEA SI,S1+2
MOV CL,N1

CONCATENATE_LOOP1:
MOV DL,[SI]
MOV [DI],DL
INC SI
INC DI
DEC CL
JNZ CONCATENATE_LOOP1

LEA SI,S2+2
MOV CH,N2

CONCATENATE_LOOP2:
MOV DH,[SI]
MOV [DI],DH
INC SI
INC DI
DEC CH
JNZ CONCATENATE_LOOP2

DISP_WORD M7
DISPLAY_STRING S3
LEA SI,S3+1
MOV BL,[SI]
MOV N3,BL
ADD BL,30H
DISP_WORD M8
DISP_ASCII BL

RET
ENDP


checkEqual PROC

DISP_WORD M9
LEA SI,S1+2
LEA DI,S2+2
MOV CL,N1
MOV CH,N2

CMP CL,CH
JNZ NOT_EQUAL
EQUAL_LOOP:
MOV DL,[SI]
MOV DH,[DI]
CMP DL,DH
JNZ NOT_EQUAL
INC DI
INC SI
DEC CL
JNZ EQUAL_LOOP
DISP_WORD M10
JMP LAST
NOT_EQUAL:
DISP_WORD M11

RET
ENDP


LAST:
.EXIT
END
